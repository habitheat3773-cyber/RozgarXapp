// Background task dispatcher
@pragma('vm:entry-point')
void callbackDispatcher() {
  Workmanager().executeTask((task, inputData) async {
    await Firebase.initializeApp(options: DefaultFirebaseOptions.currentPlatform);
    switch (task) {
      case BackgroundService.fetchJobsTask:
        await BackgroundService.fetchAndSyncJobs();
        break;
      case BackgroundService.cleanExpiredJobsTask:
        await BackgroundService.cleanExpiredJobs();
        break;
      case BackgroundService.sendDeadlineRemindersTask:
        await BackgroundService.sendDeadlineReminders();
        break;
    }
    return Future.value(true);
  });
}

Future<void> main() async {
  await runZonedGuarded(
    () async {
      WidgetsFlutterBinding.ensureInitialized();

      // System UI configuration
      SystemChrome.setSystemUIOverlayStyle(
        const SystemUiOverlayStyle(
          statusBarColor: Colors.transparent,
          statusBarIconBrightness: Brightness.light,
          navigationBarColor: Colors.transparent,
        ),
      );
      SystemChrome.setPreferredOrientations([
        DeviceOrientation.portraitUp,
        DeviceOrientation.portraitDown,
      ]);

      // Initialize Firebase
      await Firebase.initializeApp(
        options: DefaultFirebaseOptions.currentPlatform,
      );

      // Initialize Crashlytics
      FlutterError.onError = FirebaseCrashlytics.instance.recordFlutterFatalError;

      // Initialize Hive local database
      await Hive.initFlutter();
      await _registerHiveAdapters();
      await _openHiveBoxes();

      // Initialize services
      await NotificationService.initialize();
      await AdMobService.initialize();
      await AnalyticsService.initialize();

      // Initialize background worker (auto job fetch every 3 hours)
      await Workmanager().initialize(callbackDispatcher, isInDebugMode: false);
      await _scheduleBackgroundTasks();

      // Setup dependency injection
      await setupDependencies();

      AppLogger.info('✅ RozgarX initialized successfully');

      runApp(
        const ProviderScope(
          child: RozgarXApp(),
        ),
      );
    },
    (error, stack) => FirebaseCrashlytics.instance.recordError(error, stack, fatal: true),
  );
}

