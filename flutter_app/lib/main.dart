Future<void> _openHiveBoxes() async {
  await Hive.openBox('settings');
  await Hive.openBox('cached_jobs');
  await Hive.openBox('user_data');
  await Hive.openBox('offline_jobs');
  await Hive.openBox('exam_schedule');
  await Hive.openBox('syllabus_cache');
}

Future<void> _scheduleBackgroundTasks() async {
  // Auto fetch new jobs every 3 hours
  await Workmanager().registerPeriodicTask(
    'fetch_jobs_periodic',
    BackgroundService.fetchJobsTask,
    frequency: const Duration(hours: 3),
    constraints: Constraints(
      networkType: NetworkType.connected,
    ),
    backoffPolicy: BackoffPolicy.linear,
    backoffPolicyDelay: const Duration(minutes: 15),
  );

  // Clean expired jobs daily at midnight
  await Workmanager().registerPeriodicTask(
    'clean_expired_jobs',
    BackgroundService.cleanExpiredJobsTask,
    frequency: const Duration(hours: 24),
    initialDelay: const Duration(hours: 1),
  );

  // Send deadline reminders daily at 8am
  await Workmanager().registerPeriodicTask(
    'deadline_reminders',
    BackgroundService.sendDeadlineRemindersTask,
    frequency: const Duration(hours: 24),
    initialDelay: const Duration(hours: 2),
  );
}
